void solve()
{
    int n, ct = 0;
    cin >> n;
    vector<int> v(n), v1(n);
    cinv(v, n);
    if (n == 1)
    {
        print(1);
        print(0);
        return;
    }
    int mex = 0;
    vector<int> v2(1e6);
    for (int i = n - 1; i >= 0; i--)
    {
        v2[v[i]]++;
        while (v2[mex])
            mex++;
        v1[i] = mex;
    }
    set<int> s;
    for (int i = 0; i < v1[0]; i++)
        s.insert(i);
    vector<int> ans{v1[0]};
    while (ct < n)
    {
        if (s.empty())
        {
            ans.pb(v1[ct]);
            for (int j = 0; j < v1[ct]; j++)
                s.insert(j);
        }
        if (s.find(v[ct]) != s.end())
            s.erase(v[ct]);
        ct++;
    }
    print(len(ans));
    pv(ans);
}

