ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    ll n;
    cin >> n;
    vector<ll> a(n);
    cinv(a, n);
    vector<ll> b(n);
    cinv(b, n);
    map<ll, ll> ma;
    map<ll, ll> mb;
    ll same = 0;
    for (int i = 0; i < n; i++)
    {
        ma[a[i]]++;
        mb[b[i]]++;
        if (a[i] == b[i])
            same++;
    }
    ll common = 0;
    for (auto i : ma)
    {
        if (mb.find(i.first) != mb.end())
            common += min(i.second, mb[i.first]);
    }
    print(same);
    print(common - same);
}

int32_t main()
{

    int tc = 1;
    // cin >> tc;
    while (tc--)
        solve();
    return 0;
}