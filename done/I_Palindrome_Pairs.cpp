ll binpow(ll a, ll b)
{
    a %= MOD;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

void solve()
{
    map<ll, ll> mp;
    ll n;
    cin >> n;
    ll ans = 0;
    for (int i = 0; i < n; i++)
    {
        string s;
        cin >> s;
        ll mask = 0;
        for (char c : s)
            mask ^= (1 << (c - 'a'));
        ans += mp[mask];
        for (int i = 0; i < 26; i++)
        {
            mask ^= (1 << i);
            ans += mp[mask];
            mask ^= (1 << i);
        }
        mp[mask]++;
    }
    print(ans);
}

