vector<ll> v(1000005, 1);

ll binpow(ll a, ll b)
{
    a %= MOD;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

void solve()
{
    ll n, r;
    cin >> n >> r;
    ll num = v[n];
    ll deno = v[n - r] * v[r] % MOD;
    cout << num * binpow(deno, MOD - 2) % MOD << endl;
}

int32_t main()
{
    for (ll i = 1; i <= 1000005; i++)
        v[i] = v[i - 1] * i % MOD;
    int tc = 1;
    cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}