ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

int maxSatisfaction(vector<int> &satisfaction)
{
    sort(all(satisfaction));
    int idx = lower_bound(all(satisfaction), 0) - satisfaction.begin();
    int sm = 0, nidx = 0;
    for (int i = idx; i < satisfaction.size(); i++)
        sm += satisfaction[i];
    for (int i = idx - 1; i >= 0; i--)
    {
        if (satisfaction[i] + sm < 0)
            break;
        sm += satisfaction[i];
        nidx++;
    }
    int ans = 0, j = 1;
    for (int i = idx - nidx; i < satisfaction.size(); i++)
    {
        ans += j * satisfaction[i];
        j += 1;
    }
    return ans;
}

