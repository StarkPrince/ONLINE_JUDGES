ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    int n, m, u, v;
    cin >> n >> m;
    unordered_set<int> a[n];
    for (int i = 0; i < m; i++)
    {
        cin >> u >> v;
        a[u - 1].insert(v - 1);
        a[v - 1].insert(u - 1);
    }

    vector<bool> visited(n, false);
    for (int i = 0; i < n; i++)
    {
        if (!visited[i])
        {
            unordered_set<int> group = a[i];
            group.insert(i);

            for (auto j : group)
            {
                if (a[j].size() < group.size() - 1)
                {
                    cout << "NO" << endl;
                    return;
                }
                for (auto k : a[j])
                {
                    if (group.find(k) == group.end())
                    {
                        cout << "NO" << endl;
                        return;
                    }
                }
                visited[j] = true;
            }
        }
    }
    cout << "YES" << endl;
}

int32_t main()
{

    int tc = 1;
    // cin >> tc;
    while (tc--)
        solve();
    return 0;
}