void solve()
{
    int n;
    cin >> n;
    vector<int> v(n);
    cinv(v, n);
    int l = 0, r = n - 1;
    // sort the array and store it in a new array
    vector<int> v1 = v;
    sort(all(v1));
    int high = n - 1, low = 0;
    while (l < r && low < high)
    {
        if (v[l] == v1[low])
        {
            l++;
            low++;
        }
        else if (v[l] == v1[high])
        {
            l++;
            high--;
        }
        else if (v[r] == v1[low])
        {
            r--;
            low++;
        }
        else if (v[r] == v1[high])
        {
            r--;
            high--;
        }
        else
            break;
    }
    if (l < r)
        cout << l + 1 << " " << r + 1 << endl;
    else
        cout << -1 << endl;
}

