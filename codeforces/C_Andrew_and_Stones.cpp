ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    cinv(a, n);
    if (n == 3 && a[1] % 2 == 1)
    {
        cout << "-1" << endl;
        return;
    }
    int cnt1 = 0, cnt2 = 0;
    for (int i = 1; i < n - 1; i++)
    {
        if (a[i] == 1)
            cnt1++;
        cnt2 += (a[i] + 1) / 2;
    }
    if (cnt1 == n - 2)
        cout << "-1" << endl;
    else
        cout << cnt2 << endl;
}

