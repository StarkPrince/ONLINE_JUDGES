ll binpow(ll a, ll b)
{
    a %= MOD;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % MOD;
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}
int most_significant_bit(int n)
{
    int ans = 0;
    while (n)
    {
        n >>= 1;
        ans++;
    }
    return ans;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> v(n);
    cinv(v, n);
    vector<int> mp(32);
    for (auto i : v)
        mp[most_significant_bit(i)]++;
    ll ans = 0;
    for (auto i : mp)
        ans += i * (i - 1) / 2;
    print(ans);
}

