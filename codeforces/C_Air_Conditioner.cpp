ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    ll n, m;
    cin >> n >> m;
    vector<vector<ll>> td;
    for (int i = 0; i < n; i++)
    {
        vector<ll> temp;
        for (int j = 0; j < 3; j++)
        {
            ll t;
            cin >> t;
            temp.push_back(t);
        }
        td.push_back(temp);
    }
    sort(all(td));
    ll lo = m, hi = m;
    ll time = 0;
    bool bl = true;
    for (int i = 0; i < n; i++)
    {
        ll dist = td[i][0] - time;
        time += dist;
        lo = max(td[i][1], lo - dist);
        hi = min(td[i][2], hi + dist);
        if (lo > hi)
        {
            bl = false;
            break;
        }
    }
    if (bl)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
}

int32_t main()
{

    int tc = 1;
    cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}