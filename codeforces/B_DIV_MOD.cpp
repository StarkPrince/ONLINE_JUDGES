ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    ll l, r, a;
    cin >> l >> r >> a;
    // blunt answer
    ll ans = (r / a) + r % a;
    // extra values
    ll ex = r % a;
    ll dec = 0;
    if (ex != a - 1)
        dec = ex + 1;

    ll val = max(r - dec, l);
    ans = max(ans, val / a + val % a);
    cout << ans << "\n";
}

int32_t main()
{

    int tc = 1;
    cin >> tc;
    while (tc--)
    {
        solve();
    }
    return 0;
}