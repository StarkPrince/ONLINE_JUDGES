ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    ll n;
    cin >> n;
    vector<ll> a(n);
    cinv(a, n);
    ll s = accumulate(all(a), 0);
    ll m = *max_element(all(a));
    ll k = ceil((double)s / (n - 1));
    cout << (max(k, m) * (n - 1)) - s << endl;
}

