ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    cinv(a, n);
    int x = *max_element(all(a));
    vector<vector<pair<int, int>>> sum(2 * x + 1);
    for (int len = 2; len <= n; len++)
    {
        for (int i = 0; i + len - 1 < n; i++)
        {
            int j = i + len - 1;
            int s = a[i] + a[j];
            sum[s].push_back({i + 1, j + 1});
            for (auto ij : sum[s])
            {
                int i1 = ij.first, j1 = ij.second;
                for (auto ji : sum[s])
                {
                    int i2 = ji.first, j2 = ji.second;
                    if (i1 != i2 && i1 != j2 && j1 != i2 && j1 != j2)
                    {
                        print("YES");
                        print(i1, j1, i2, j2);
                        return;
                    }
                }
            }
        }
    }
    print("NO");
}

