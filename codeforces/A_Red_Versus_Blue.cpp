ll handle_mod(ll n, ll mod = MOD)
{
    return (n < 0 ? mod - abs(n % mod) : n) % mod;
}

ll binpow(ll a, ll b, ll m = 1e18)
{
    a %= m;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}

void solve()
{
    int n, r, b;
    cin >> n >> r >> b;
    int mt = r % (b + 1);
    for (int i = 0; i < b; i++)
    {
        for (int i = 0; i < r / (b + 1); i++)
            cout << "R";
        if (i < mt)
            cout << "R";
        cout << "B";
    }
    for (int i = 0; i < r / (b + 1); i++)
        cout << "R";
    cout << endl;
}

